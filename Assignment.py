plt.title('Distribution of Transaction Quantity')
plt.show()

# Boxplot of Value ($)
plt.figure(figsize=(8,6))
transactions.boxplot(column='Value ($)')
plt.title('Boxplot of Transaction Value')
plt.show()

# Trend Analysis: Aggregate daily sales
daily_sales = transactions.groupby('Date')['Quantity'].sum()
daily_sales.plot(figsize=(10, 5), title='Daily Quantity Sold')
plt.xlabel('Date')
plt.ylabel('Total Quantity')
plt.show()